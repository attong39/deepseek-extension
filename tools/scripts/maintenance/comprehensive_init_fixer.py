RESULTS: list[dict[str, Any]] = []


class InitFileFixer:
    """Comprehensive __init__.py fixer."""

    def __init__(self, root_path: Path) -> None:
        """Initialize fixer."""
        self.root = root_path
        self.backup_dir = root_path / ".init_fix_backups"
        self.backup_dir.mkdir(exist_ok=True)

    def find_python_packages(self) -> list[Path]:
        """Find all Python packages that need __init__.py files."""
        packages = set()
        skip_dirs = {
            ".venv",
            "__pycache__",
            ".git",
            ".pytest_cache",
            ".mypy_cache",
            ".ruff_cache",
            "node_modules",
            "build",
            "dist",
            ".init_fix_backups",
            ".safe_fix_backups_v3",
            ".copilot",
            "desktop_ai_zeta",
        }
        for py_file in self.root.rglob("*.py"):
            parent = py_file.parent
            if any(part.startswith(".") or part in skip_dirs for part in parent.parts):
                continue
            if parent != self.root and parent.name != "__pycache__":
                packages.add(parent)
        return sorted(packages)

    def create_missing_init_files(self) -> None:
        """Create missing __init__.py files."""
        packages = self.find_python_packages()
        for package_dir in packages:
            init_file = package_dir / "__init__.py"
            if not init_file.exists():
                relative_path = package_dir.relative_to(self.root)
                package_name = str(relative_path).replace("\\", ".").replace("/", ".")
                content = f'''"""
{package_name} package.
Auto-generated by comprehensive_init_fixer.py
"""
__all__: list[str] = []
'''
                init_file.write_text(content, encoding="utf-8")
                RESULTS.append(
                    {
                        "package": str(relative_path),
                        "action": "CREATED",
                        "init_file": str(init_file.relative_to(self.root)),
                        "all_exports": [],
                        "imports_updated": 0,
                        "status": "✅ CREATED",
                    }
                )

    def analyze_python_file(self, file_path: Path) -> dict[str, Any]:
        """Analyze a Python file to extract public API."""
        try:
            content = file_path.read_text(encoding="utf-8")
            tree = ast.parse(content)
            exports = []
            for node in ast.walk(tree):
                if isinstance(node, (ast.FunctionDef, ast.ClassDef)):
                    if not node.name.startswith("_"):
                        exports.append(node.name)
                elif isinstance(node, ast.Assign):
                    for target in node.targets:
                        if isinstance(target, ast.Name) and not target.id.startswith("_"):
                            exports.append(target.id)
            return {"exports": list(set(exports)), "error": None}
        except Exception as e:
            return {"exports": [], "error": str(e)}

    def fix_existing_init_file(self, init_file: Path) -> None:
        """Fix an existing __init__.py file."""
        try:
            backup_file = self.backup_dir / f"{init_file.name}_{init_file.parent.name}.bak"
            backup_file.write_text(init_file.read_text(encoding="utf-8"), encoding="utf-8")
            content = init_file.read_text(encoding="utf-8")
            package_dir = init_file.parent
            relative_path = package_dir.relative_to(self.root)
            package_name = str(relative_path).replace("\\", ".").replace("/", ".")
            all_exports = []
            for py_file in package_dir.glob("*.py"):
                if py_file.name != "__init__.py":
                    analysis = self.analyze_python_file(py_file)
                    all_exports.extend(analysis["exports"])
            all_exports = sorted(set(all_exports))
            lines = []
            lines.append('"""')
            lines.append(f"{package_name} package.")
            lines.append("")
            lines.append("Auto-fixed by comprehensive_init_fixer.py")
            lines.append('"""')
            lines.append("")
            lines.append("from __future__ import annotations")
            lines.append("")
            existing_imports = self.extract_imports(content)
            for imp in existing_imports:
                if not imp.startswith("from __future__"):
                    lines.append(imp)
            if existing_imports:
                lines.append("")
            if all_exports:
                lines.append("__all__ = [")
                for export in all_exports:
                    lines.append(f'    "{export}",')
                lines.append("]")
            else:
                lines.append("__all__: list[str] = []")
            lines.append("")
            new_content = "\n".join(lines)
            init_file.write_text(new_content, encoding="utf-8")
            RESULTS.append(
                {
                    "package": str(relative_path),
                    "action": "FIXED",
                    "init_file": str(init_file.relative_to(self.root)),
                    "all_exports": all_exports,
                    "imports_updated": len(existing_imports),
                    "status": "✅ FIXED",
                }
            )
        except Exception as e:
            RESULTS.append(
                {
                    "package": str(relative_path),
                    "action": "ERROR",
                    "init_file": str(init_file.relative_to(self.root)),
                    "all_exports": [],
                    "imports_updated": 0,
                    "status": f"❌ ERROR: {e}",
                }
            )

    def extract_imports(self, content: str) -> list[str]:
        """Extract import statements from content."""
        try:
            tree = ast.parse(content)
            imports = []
            for node in ast.walk(tree):
                if isinstance(node, ast.Import):
                    for alias in node.names:
                        imports.append(f"import {alias.name}")
                elif isinstance(node, ast.ImportFrom):
                    if node.module:
                        if node.names[0].name == "*":
                            continue  # Skip wildcard imports
                        names = [alias.name for alias in node.names]
                        imports.append(f"from {node.module} import {', '.join(names)}")
            return imports
        except Exception:
            return []

    def fix_all_init_files(self) -> None:
        """Fix all __init__.py files in the project."""
        print("🔧 Creating missing __init__.py files...")
        self.create_missing_init_files()
        print("🔧 Fixing existing __init__.py files...")
        for init_file in self.root.rglob("__init__.py"):
            if any(part.startswith(".") for part in init_file.parts):
                continue
            if "build" in str(init_file) or "dist" in str(init_file):
                continue
            self.fix_existing_init_file(init_file)


def run_quality_checks() -> dict[str, Any]:
    """Run quality checks as requested."""
    print("\n🔍 Running quality checks...")
    results = {}
    print("  Running ruff E999 --fix...")
    try:
        result = subprocess.run(
            [sys.executable, "-m", "ruff", "check", ".", "--select=E999", "--fix"],
            capture_output=True,
            text=True,
            cwd=ROOT,
            timeout=60,
        )
        results["ruff_syntax"] = {
            "returncode": result.returncode,
            "stdout": result.stdout,
            "stderr": result.stderr,
        }
    except Exception as e:
        results["ruff_syntax"] = {"error": str(e)}
    print("  Running isort...")
    try:
        result = subprocess.run(
            [sys.executable, "-m", "isort", "."],
            capture_output=True,
            text=True,
            cwd=ROOT,
            timeout=60,
        )
        results["isort"] = {
            "returncode": result.returncode,
            "stdout": result.stdout,
            "stderr": result.stderr,
        }
    except Exception as e:
        results["isort"] = {"error": str(e)}
    print("  Running autoflake...")
    try:
        result = subprocess.run(
            [
                sys.executable,
                "-m",
                "autoflake",
                "-r",
                "--remove-all-unused-imports",
                "--in-place",
                ".",
            ],
            capture_output=True,
            text=True,
            cwd=ROOT,
            timeout=120,
        )
        results["autoflake"] = {
            "returncode": result.returncode,
            "stdout": result.stdout,
            "stderr": result.stderr,
        }
    except Exception as e:
        results["autoflake"] = {"error": str(e)}
    print("  Running compileall...")
    try:
        result = subprocess.run(
            [sys.executable, "-m", "compileall", "."],
            capture_output=True,
            text=True,
            cwd=ROOT,
            timeout=60,
        )
        results["compileall"] = {
            "returncode": result.returncode,
            "stdout": result.stdout,
            "stderr": result.stderr,
        }
    except Exception as e:
        results["compileall"] = {"error": str(e)}
    print("  Skipping mypy for now...")
    results["mypy"] = {"skipped": True}
    print("  Skipping pytest for now...")
    results["pytest"] = {"skipped": True}
    return results


def print_report() -> None:
    """Print comprehensive report."""
    print("\n" + "=" * 80)
    print("📊 COMPREHENSIVE __init__.py FIXER REPORT")
    print("=" * 80)
    created = len([r for r in RESULTS if r["action"] == "CREATED"])
    fixed = len([r for r in RESULTS if r["action"] == "FIXED"])
    errors = len([r for r in RESULTS if r["action"] == "ERROR"])
    print("\n📈 SUMMARY:")
    print(f"  Created: {created} files")
    print(f"  Fixed:   {fixed} files")
    print(f"  Errors:  {errors} files")
    print(f"  Total:   {len(RESULTS)} files")
    print("\n📋 DETAILED RESULTS:")
    print(f"{'Package':<40} {'Action':<8} {'__all__':<6} {'Imports':<8} {'Status':<20}")
    print("-" * 88)
    for result in RESULTS[:20]:  # Show first 20
        package = result["package"][:38]
        action = result["action"][:6]
        all_count = len(result["all_exports"])
        imports = result["imports_updated"]
        status = result["status"][:18]
        print(f"{package:<40} {action:<8} {all_count:<6} {imports:<8} {status:<20}")
    if len(RESULTS) > 20:
        print(f"... and {len(RESULTS) - 20} more files")


def main() -> int:
    """Main entry point."""
    print("🚀 COMPREHENSIVE __init__.py FIXER")
    print("=" * 50)
    fixer = InitFileFixer(ROOT)
    fixer.fix_all_init_files()
    quality_results = run_quality_checks()
    print_report()
    print("\n🔍 QUALITY CHECK RESULTS:")
    for tool, result in quality_results.items():
        if "error" in result:
            print(f"  {tool}: ❌ ERROR - {result['error']}")
        elif "skipped" in result:
            print(f"  {tool}: ⏭️ SKIPPED")
        else:
            status = "✅ OK" if result["returncode"] == 0 else f"❌ FAILED ({result['returncode']})"
            print(f"  {tool}: {status}")
    print("\n✅ Comprehensive __init__.py fixing completed!")
    print(f"📁 Backups saved in: {fixer.backup_dir}")
    return 0


if __name__ == "__main__":
    sys.exit(main())
