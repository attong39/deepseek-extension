"""
Copilot-aware refactor tool:
- Đọc PROJECT_MAP.md để biết mapping module → đích
- Chạy ruff/mypy/pytest nhanh để chấm điểm file 'dùng được'
- Di chuyển file pass về cấu trúc mới (dry-run/commit)
- Sinh stub (docstring + TODO) cho file còn thiếu theo mapping
Usage:
  uv run python tools/refactor/copilot_refactor.py --dry-run
"""

