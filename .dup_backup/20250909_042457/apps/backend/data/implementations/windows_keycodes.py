# Common hotkey combinations
def ctrl_c() -> tuple[int, int]:
    return (VK_CTRL, VK_C)


def ctrl_v() -> tuple[int, int]:
    return (VK_CTRL, VK_V)


def ctrl_a() -> tuple[int, int]:
    return (VK_CTRL, VK_A)


def alt_tab() -> tuple[int, int]:
    return (VK_ALT, VK_TAB)


def win_r() -> tuple[int, int]:
    return (VK_LWIN, VK_R)
